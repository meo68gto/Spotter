#!/usr/bin/env node
import { readdir, stat, writeFile } from 'node:fs/promises';
import { join, relative, resolve } from 'node:path';

const repoRoot = resolve(new URL('../..', import.meta.url).pathname);
const screensRoot = join(repoRoot, 'apps/mobile/src/screens');
const outputPath = join(repoRoot, 'docs/qa/_generated-screen-index.md');

const walk = async (dir) => {
  const entries = await readdir(dir);
  const files = [];
  for (const entry of entries) {
    const absolute = join(dir, entry);
    const info = await stat(absolute);
    if (info.isDirectory()) {
      files.push(...(await walk(absolute)));
      continue;
    }
    if (entry.endsWith('.tsx') || entry.endsWith('.ts')) files.push(absolute);
  }
  return files;
};

const isScreenFile = (path) => /Screen\.(tsx|ts)$/.test(path) || /Screen\/(index\.(tsx|ts))$/.test(path);

const files = (await walk(screensRoot))
  .filter(isScreenFile)
  .sort((a, b) => a.localeCompare(b));

const lines = [
  '# Generated Mobile Screen Index',
  '',
  '> Auto-generated by scripts/qa/generate-mobile-screen-checklist.mjs',
  '',
  '| Screen File | Area |',
  '| --- | --- |'
];

for (const file of files) {
  const rel = relative(repoRoot, file);
  const area = rel.includes('/auth/')
    ? 'Auth'
    : rel.includes('/onboarding/')
      ? 'Onboarding'
      : rel.includes('/dashboard/inbox/')
        ? 'Inbox'
        : rel.includes('/dashboard/coaching/')
          ? 'Coaching'
          : rel.includes('/dashboard/profile/')
            ? 'Profile'
            : rel.includes('/dashboard/')
              ? 'Dashboard'
              : 'Core';
  lines.push(`| ${rel} | ${area} |`);
}

await writeFile(outputPath, `${lines.join('\n')}\n`, 'utf8');
console.log(`Wrote ${files.length} screen entries to ${relative(repoRoot, outputPath)}`);
